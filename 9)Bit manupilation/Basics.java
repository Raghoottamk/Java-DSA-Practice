public class Basics {
    public static void main(String[] args) {
        //binary AND
        System.out.println("binary AND");
        System.out.println(5 & 6);
        //binary OR
        System.out.println("binary OR");
        System.out.println(5 | 6);
        //bianry XOR
        System.out.println("bianry XOR");
        System.out.println(5 ^ 6);
        //binary complement
        System.out.println("binary complement");
        System.out.println(~1);
        //binary left shift(<<)
        System.out.println("binary left shift(<<)");
        System.out.println(5<<0);//left shift by 0 places
        System.out.println(5<<1);//left shift by 1 places
        System.out.println(5<<2);//left shift by 2 places
        //binary right shift
        System.out.println("binary right shift(<<)");
        System.out.println(5>>1);//right shift by 1 places
        System.out.println(5>>0);//right shift by 0 places
        System.out.println(5>>2);//right shift by 2 places
    }    
}
